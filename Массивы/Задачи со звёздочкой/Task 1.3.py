# Создать одномерный массив на 10 целых чисел и заполнить его целыми числами от 1 до 10 случайным образом. 
# Отсортировать элементы массива в порядке возрастания и найти элемент, находящийся на 4 позиции, после чего вывести его в консоль.

import random

def create_and_sort_array():
    # Создать список целых чисел от 1 до 10
    numbers = list(range(1, 11))
    
    # Перемешать список для случайного порядка
    random.shuffle(numbers)
    
    # Вывести исходный массив
    print("Исходный массив:")
    print(numbers)
    
    # Отсортировать массив в порядке возрастания
    numbers.sort()
    
    # Вывести отсортированный массив
    print("\nОтсортированный массив:")
    print(numbers)
    
    # Найти элемент на 4-й позиции
    if len(numbers) >= 4:
        fourth_element = numbers[3]  # Индекс 3 соответствует 4-й позиции
        print(f"\nЭлемент на 4-й позиции: {fourth_element}")
    else:
        print("\nМассив слишком короткий.")

# Выполнить функцию
create_and_sort_array()
