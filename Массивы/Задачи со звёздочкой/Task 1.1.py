# Нужно создать двухмерный массив на 8х8 целых чисел и заполнить его случайными значениями в диапазоне от 0 до 100. 
# Посчитать сумму элементов всех четных строк массива и вывести ее в консоль (примечание: пользователь сам вводит с клавиатуры числа в массив)

def create_and_sum_array():
    # Создать двухмерный массив
    array = []
    
    # Заполнить массив значениями, введенными пользователем
    for i in range(8):
        row = []
        for j in range(8):
            while True:
                try:
                    value = int(input(f"Введите значение для строки {i+1}, столбца {j+1}: "))
                    if 0 <= value <= 100:
                        row.append(value)
                        break
                    else:
                        print("Пожалуйста, введите значение от 0 до 100.")
                except ValueError:
                    print("Пожалуйста, введите целое число.")
        array.append(row)
    
    # Вывести массив
    print("\nВведенный массив:")
    for row in array:
        print(row)
    
    # Посчитать сумму элементов четных строк
    even_rows_sum = sum(sum(row) for i, row in enumerate(array) if (i + 1) % 2 == 0)
    
    # Вывести сумму
    print(f"\nСумма элементов четных строк: {even_rows_sum}")

# Выполнить функцию
create_and_sum_array()
