# Задан массив целых чисел A[20]. Создать другой массив целых чисел B[ ], 
# в который войдут все числа исходного массива, удовлетворяющие условию: А[i] <= 888, после чего отсортировать элементы массива В[ ] по убыванию.

def create_and_sort_array():
    # Ввести массив A
    A = []
    for i in range(20):
        while True:
            try:
                value = int(input(f"Введите {i+1}-е значение массива A: "))
                A.append(value)
                break
            except ValueError:
                print("Пожалуйста, введите целое число.")
    
    # Создать массив B
    B = [value for value in A if value <= 888]
    
    # Отсортировать массив B по убыванию
    B.sort(reverse=True)
    
    # Вывести массивы
    print("\nИсходный массив A:")
    print(A)
    print("\nМассив B (элементы <= 888) в порядке убывания:")
    print(B)

# Выполнить функцию
create_and_sort_array()
