# Сформируйте одномерный массив действительных чисел как геометрическую последовательность со знаменателем ½, 
# начиная с 1, т.е.: 1,0 0,5 0,25 0,125 и т.д. Чему будет равна сумма 100 его членов с точностью до 10-10?

def calculate_geometric_sum():
    a1 = 1  # Первый член
    q = 0.5  # Знаменатель прогрессии
    n = 100  # Количество членов
    
    # Рассчитать сумму
    sum_n = a1 * (1 - q**n) / (1 - q)
    
    return sum_n

# Выполнить функцию
result = calculate_geometric_sum()
print("Сумма 100 членов геометрической прогрессии:", round(result, 10))
