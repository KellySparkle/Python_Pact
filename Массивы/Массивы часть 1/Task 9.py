# Вычислите сумму членов последовательности, начинающейся с единицы, 
# в которой каждый следующий член в три раза больше предыдущего, т.е. {1, 3, 9, 27, 81,…}, 
# причем последний член последовательности не должен превышать 1000.

def calculate_sequence_sum():
    sequence = [1]
    while sequence[-1] * 3 <= 1000:
        sequence.append(sequence[-1] * 3)
    
    # Рассчитать сумму членов последовательности
    total_sum = sum(sequence)
    
    return total_sum

# Выполнить функцию
result = calculate_sequence_sum()
print("Сумма членов последовательности:", result)
