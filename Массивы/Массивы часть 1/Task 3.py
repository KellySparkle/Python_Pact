# В заданной строке текста определите количество слов. Каждое слово отделено друг от друга пробелом.

def count_words(text):
    # Разделить строку на слова
    words = text.split()
    
    # Вывести количество слов
    return len(words)

# Запросить строку у пользователя
text = input("Введите строку текста: ")

# Вывести количество слов
print("Количество слов в строке:", count_words(text))
