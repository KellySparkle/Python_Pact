# Задан двумерный массив чисел 
# (используйте генератор случайных чисел). 
# Отсортируйте каждую нечетную строку по возрастанию, а каждую четную – по убыванию, 
# после чего выведите значение k-го столбца.

import numpy as np

def sort_and_extract_column():
    # Сгенерировать двумерный массив 10х10 с случайными значениями
    array = np.random.randint(0, 100, size=(10, 10))
    
    # Вывести исходный массив
    print("Исходный массив:")
    print(array)
    
    # Отсортировать каждую нечетную строку по возрастанию и каждую четную по убыванию
    for i in range(array.shape[0]):
        if (i + 1) % 2 != 0:  # Нечетная строка
            array[i] = np.sort(array[i])
        else:  # Четная строка
            array[i] = np.sort(array[i])[::-1]
    
    # Вывести отсортированный массив
    print("\nОтсортированный массив:")
    print(array)
    
    # Запросить номер столбца k
    k = int(input("Введите номер столбца k (от 1 до 10): "))
    
    # Проверить корректность номера столбца
    if 1 <= k <= 10:
        # Вывести значения k-го столбца
        print(f"\nЗначения {k}-го столбца:")
        print(array[:, k - 1])
    else:
        print("Номер столбца должен быть от 1 до 10.")

# Выполнить функцию
sort_and_extract_column()
