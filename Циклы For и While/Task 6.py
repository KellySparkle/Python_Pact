# Организовать беспрерывный ввод чисел с клавиатуры, пока пользователь не введёт 0. 
# После ввода нуля, показать на экран количество чисел, которые были введены, их общую сумму и среднее арифметическое. 
# Подсказка: необходимо объявить переменную-счетчик, которая будет считать количество введенных чисел, 
# и переменную, которая будет накапливать общую сумму чисел.

def process_numbers():
    """
    Обрабатывает ввод чисел до 0 и выводит статистику.
    """
    count = 0  # Счётчик количества чисел
    total_sum = 0  # Переменная для накопления суммы
    
    while True:
        try:
            number = float(input("Введите число (0 для завершения): "))
            
            if number == 0:
                break
            
            total_sum += number
            count += 1
        except ValueError:
            print("Некорректный ввод. Пожалуйста, введите число.")
    
    if count == 0:
        print("Вы не ввели ни одного числа.")
    else:
        average = total_sum / count
        print(f"Количество введенных чисел: {count}")
        print(f"Общая сумма: {total_sum}")
        print(f"Среднее арифметическое: {average}")

def main():
    while True:
        process_numbers()
        
        cont = input("Продолжить ввод? (y/n): ")
        if cont.lower() != 'y':
            print("Программа завершена.")
            break

if __name__ == "__main__":
    main()
