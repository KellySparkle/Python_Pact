# Напишите программу, которая вводит два целых числа и находит их произведение, 
# не используя операцию умножения. Учтите, что числа могут быть отрицательными. 
# Входные данные: входная строка содержит два целых числа. Выходные данные: программа должна вывести произведение введённых чисел.

def multiply(a, b):
    # Результат
    result = 0
    
    # Определение знака результата
    negative = (a < 0 and b > 0) or (a > 0 and b < 0)
    
    # Приведение обоих чисел к положительным
    a, b = abs(a), abs(b)
    
    # Сложение a столько раз, сколько равно b
    for _ in range(b):
        result += a
    
    # Если результат должен быть отрицательным
    if negative:
        result = -result
    
    return result

# Ввод двух целых чисел
a, b = map(int, input("Введите два целых числа через пробел: ").split())

# Вывод результата
print("Произведение введённых чисел:", multiply(a, b))
