# Напишите программу, в которой в цикле do-while 
# пользователь вводит по одному символу, а программа подсчитывает количество введенных символов.
#  Когда пользователь вводит точку, ввод заканчивается, и программа выводит пользователю число введенных символов 
# (не включая финальную точку)

def count_characters():
    """
    Подсчитывает количество введенных символов до точки.
    """
    count = 0
    while True:
        char = input("Введите символ (введите точку для завершения): ")
        
        if char == '.':
            break
        
        count += len(char)
    
    print(f"Вы ввели {count} символов.")

def main():
    while True:
        count_characters()
        
        cont = input("Продолжить? (y/n): ")
        if cont.lower() != 'y':
            print("Программа завершена.")
            break

if __name__ == "__main__":
    main()
